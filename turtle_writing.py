# ----------- Hopefully the only functions we'll need to edit ------------
    
def pendown():
    t.pendown()

def penup():
    t.penup()

def forward(distance):
    t.forward(distance * scale)

def left(angle):
    deg = angle * 180 / math.pi
    t.left(deg)

# ------------------------------------------------------------------------

def point_to_point(rotation,p0,p1):
    dx = p1[0] - p0[0]
    dy = p1[1] - p0[1]

    dist = math.hypot(dx,dy)
    angle = math.atan2(dy,dx)
    delta = angle - rotation
    delta = (delta + math.pi) % (2*math.pi) - math.pi

    left(delta)
    forward(dist)

    return angle

def write_letter(letter):
    # move to start point
    rot = point_to_point(0, letter_points[letter][0], letter_points[letter][1])
    pendown()

    # draw letter
    for i in range(1,len(letter_points[letter])-2):
        rot = point_to_point(rot, letter_points[letter][i], letter_points[letter][i+1])

    # move to end point
    penup()
    rot = point_to_point(rot, letter_points[letter][-2], letter_points[letter][-1])
    left(-rot)

penup()

for letter in input("Enter text to write: ").upper():
    write_letter(letter)

